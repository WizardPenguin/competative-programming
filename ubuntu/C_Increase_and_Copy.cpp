// instead we can have serach space as root(N) - 5 TO root(N) + 5
void solve(){
    ld val; 
    cin>>val; 
    if(val == 1){
        cout<<0<<ln; 
        return; 
    }
    ll ans = val; 
    for(ll i=1;i<=sqrt(val);i++){
        ll need = ceil(val/i);
        ans = min(ans,i-1 + need-1); 
    }
    cout<<ans<<endl; 
}
