void solve()
{
    // we are taking our capital to index i
    // it's better to move capital each steps till we reach index i that minimizes cost of left side
    // cost at right side remains same , If we try to move capital to some other index j
    // better would be to move as above, so we have fixed location for capital for some valid solution
    // by fixing capital we can find n solutions and return minimum one
    ll n, a, b;
    cin >> n >> a >> b;
    vll v(n);
    for (auto &elm : v)
        cin >> elm;
    ll remainingCost = 0;
    ll remainingElements = 0;
    ll minimumCost = LLONG_MAX;
    for (int i = n - 1; i >= 0; i -= 1)
    {
        ll currentCost = v[i] * (a + b) + (remainingCost - remainingElements * v[i]) * b;
        minimumCost = min(minimumCost, currentCost);
        remainingElements += 1;
        remainingCost += v[i];
    }
    // case when we don't move capital to any index
    minimumCost = min(minimumCost, remainingCost * b);
    cout << minimumCost << endl;
}
