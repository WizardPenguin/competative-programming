void solve()
{
    int n;
    cin >> n;
    vector<int> formed(n);
    set<int> remaining;
    for (int i = 1; i <= n; i += 1)
        remaining.insert(i);
    bool found = true;
    for (int i = 1; i < n; i += 2)
    {
        int val;
        cin >> val;
        if (remaining.find(val) == remaining.end())
        {
            found = false;
        }
        formed[i] = val; // this needs to be largest for making maxVal(i,i-1) = val,
        remaining.erase(val);
    }
    if (not found)
    {
        cout << -1 << endl;
        return;
    }
    // now place largest possible value fartest possible for each node
    for (int i = n - 2; i >= 0; i -= 2)
    {
        // check which is value imposing constraints
        // debug(i);
        int constraint = formed[i + 1];
        // debug(constraint);
        auto ub = remaining.upper_bound(constraint);
        if (ub == remaining.begin())
        {
            cout << -1 << endl;
            return;
        }
        // debug(*ub);
        ub--;
        formed[i] = *ub;
        remaining.erase(ub);
    }
    for (auto &elm : formed)
        cout << elm << " ";
    cout << endl;
}
