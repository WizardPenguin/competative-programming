void solve()
{
    ll n, k;
    cin >> n >> k;
    vll v(n);
    for (int i = 0; i < n; i += 1)
        cin >> v[i];
    sort(all(v));
    ll remaining = 0;
    ll minSteps = LLONG_MAX;
    for (int i = 1; i < n; i += 1)
    {
        // from i.....n-1 we are going to change them to v[0] - X
        ll elementsCount = n - i;
        ll rem = k - remaining, guess;
        // taking lower bound in case of -ve element we need to be careful since auto it takes upper bound
        if (rem < 0)
            guess = (k - remaining - elementsCount) / (elementsCount + 1);
        else
            guess = (k - remaining) / (elementsCount + 1);
        minSteps = min(minSteps, (elementsCount + max(0ll, v[0] - guess)));
        remaining += v[i];
    }
    // when we are going to change nothing
    ll guess = (k - remaining);
    minSteps = min(minSteps, (max(0ll, v[0] - guess)));
    remaining += v[0];
    if (remaining <= k)
    {
        minSteps = 0;
    }
    cout << minSteps << endl;
}
