int bitCount(int n)
{
    int count = 0;
    while (n)
    {
        count += 1;
        n >>= 1;
    }
    return count;
}
void solve()
{
    long long n;
    cin >> n;
    vi v(n);
    for (auto &elm : v)
        cin >> elm;
    int ncnt = bitCount(n);
    vector<long long> sq;
    sq.push_back(0);
    for (long long i = 1; i * i < (1 << ncnt); i += 1)
    {
        sq.push_back(i * i);
    }
    // debug(sq);
    // now find invalid pairs
    vector<int> count((1 << ncnt), 0);
    count[0] = 1;
    int curXor = 0;
    long long ans = n * (n + 1) / 2;
    // cout << ans << endl;
    for (int i = 0; i < n; i += 1)
    {
        curXor ^= v[i];
        for (auto &elm : sq)
        {
            ans -= count[elm xor curXor];
        }
        count[curXor] += 1;
    }
    cout << ans << endl;
}
