void solve()
{
    int n, q;
    cin >> n >> q;
    vi v(n);
    int maxima = 0;
    for (auto &elm : v)
        cin >> elm, maxima = max(maxima, elm);
    vector<vector<int>> winners(n);
    // debug(maxima);
    int prev = 0;
    for (int i = 1; i < n; i += 1)
    {
        if (v[prev] > v[i])
        {
            // i-1ths person wins game and value of ith person changes
            winners[prev].push_back(i); // previous has won ith battle also
        }
        else
        {
            winners[i].push_back(i);
            prev = i;
        }
    }
    debug(winners);
    while (q--)
    {
        int id, k;
        cin >> id >> k;
        id--;
        if (v[id] == maxima)
        {
            int count = upper_bound(all(winners[id]), k) - winners[id].begin();
            debug(id, count);
            if (count == winners[id].size())
            {
                count += (k - n + 1);
            }
            cout << count << endl;
        }
        else
        {
            cout << upper_bound(all(winners[id]), k) - winners[id].begin() << endl;
        }
    }
}
